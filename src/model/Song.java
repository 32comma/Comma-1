package model;

public class Song {
	private int s_id;
	private String s_title;
	private String m_id;
	private String s_artist;
	private String s_file;
	private String s_url;
	private String s_lyrics;
	private int s_age;
	public int getS_id() {
		return s_id;
	}
	public void setS_id(int s_id) {
		this.s_id = s_id;
	}
	public String getS_title() {
		return s_title;
	}
	public void setS_title(String s_title) {
		this.s_title = s_title;
	}
	public String getM_id() {
		return m_id;
	}
	public void setM_id(String m_id) {
		this.m_id = m_id;
	}
	public String getS_artist() {
		return s_artist;
	}
	public void setS_artist(String s_artist) {
		this.s_artist = s_artist;
	}
	public String getS_file() {
		return s_file;
	}
	public void setS_file(String s_file) {
		this.s_file = s_file;
	}
	public String getS_url() {
		return s_url;
	}
	public void setS_url(String s_url) {
		this.s_url = s_url;
	}
	public String getS_lyrics() {
		return s_lyrics;
	}
	public void setS_lyrics(String s_lyrics) {
		this.s_lyrics = s_lyrics;
	}
	public int getS_age() {
		return s_age;
	}
	public void setS_age(int s_age) {
		this.s_age = s_age;
	}
	
}
